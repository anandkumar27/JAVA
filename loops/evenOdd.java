package loops;
import java.util.*;
public class evenOdd {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter Your range:");
        int n = sc.nextInt();
        for(int i=0; i<=n; i++){
            // even (i%2==0)
            if (i%2!=0) {
                System.out.print(i+" ");
            }
            }
        }
  }
    

